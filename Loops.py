# Loop allows you to preform a set calculations a set number of time

# 'for' loop

# create a list-can represent anything i.e. number of apples owned
my_list = [5, 8, 10, 12]
# each individual have gained x amount of shoes, x is a RV i.e. 5
# adding 5 to each element in the list

# 'for' loop, must put a colon, must indent
for element in my_list:
    element = element + 5  # linear mapping
    print(element)

# 'If' loop
# % is the modulus/remainder sign
# '==' means if an only if
a = 15
if a % 2 == 0:
    print("a is divisible by 2")
else:
    print("a is not divisible by 2")

